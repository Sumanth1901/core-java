public class Addition
{
	public static void main(String[] add)
	{
	add(56,89);
	sub(64,8);
	mul(91,54);
	div(146,51);
	}
	
	 static void add(int a,int b)
	{
	System.out.println(a+b);
	}
	static void sub(int a,int b)
	{
	System.out.println(a-b);
	}
	static void mul(int a,int b)
	{
	System.out.println(a*b);
	}
	static void div(int a,int b)
	{
	System.out.println(a/b);
	}
}	
	

	